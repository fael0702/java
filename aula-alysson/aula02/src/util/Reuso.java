package util;

public class Reuso {
    /*
    public - visibilidade do metodo (visivel em todo o projeto)
    static - que o metodo pode ser usado sem instalar um 
    objeto, eh um metodo de classe
    void - significa que este metodo nao tem retorno
    print - nome que definimos para o metodo
    String msg -> que o metodo espera uma String como parametro
    */
    public static void print(Object msg) {
        System.out.println(msg);
    }
}
